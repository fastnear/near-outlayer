cmd_withdraw() {
    require_confidential_enabled; require_api_key; check_coordinator
    local chain="${WITHDRAW_CHAIN:-solana}"
    local to="${WITHDRAW_TO:?Set WITHDRAW_TO=<destination-chain address>}"
    local token="${WITHDRAW_TOKEN:-$SOL_USDC}"
    local amount="${WITHDRAW_AMOUNT:-$DEPOSIT_AMOUNT}"
    echo -e "${CYAN}== WITHDRAW $amount $token (confidential → $chain:$to) ==${NC}"
    local body="{\"chain\":\"$chain\",\"to\":\"$to\",\"amount\":\"$amount\",\"token\":\"$token\"}"
    echo -e "${CYAN}[1/2] dry-run…${NC}"
    parse_response "$(curl_post "/wallet/v1/confidential/withdraw/dry-run" "$body")"
    echo -e "  HTTP $RESP_CODE : $(echo "$RESP_BODY" | jq -c '.' 2>/dev/null || echo "$RESP_BODY")"
    echo -e "${CYAN}[2/2] real withdraw…${NC}"
    parse_response "$(curl_post "/wallet/v1/confidential/withdraw" "$body" "cwithdraw-$(date +%s)-$$")"
    report_op
    assert_settled "${LAST_REQUEST_ID:-}" "WITHDRAW"
}

cmd_status() {
    require_api_key
    local rid="${1:-${LAST_REQUEST_ID:-}}"
    [ -n "$rid" ] || { echo -e "${RED}Usage: $0 status <request_id>${NC}"; exit 1; }
    set +e; poll_request "$rid"; local rc=$?; set -e
    [ "$rc" = "1" ] && exit 1 || true
}

